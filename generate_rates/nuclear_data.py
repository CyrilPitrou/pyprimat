import re
from collections import Counter
from math import factorial

import numpy as np
from pypr.config import PyPRConfig

__all__ = ['detailed_balance', 'reaction_species', 'load_nubase']

def _parse_spin(jpi_field):
    """Extract the spin J as a float from a NUBASE 'Jpi' field."""
    m = re.match(r"\s*(\d+)(?:/(\d+))?", jpi_field)
    if m is None:
        raise ValueError(f"cannot parse spin from {jpi_field!r}")
    return float(m.group(1)) / float(m.group(2)) if m.group(2) else float(m.group(1))

def load_nubase(nubase_path):
    """Read mass excesses and spins of the BBN nuclides from a NUBASE2020 file."""
    # Based on _NUBASE_NAMES mapping from original pypr/nuclear_data.py
    _NUBASE_NAMES = {
        "n": "1n", "p": "1H", "H2": "2H", "H3": "3H", "He3": "3He", "He4": "4He",
        "He6": "6He", "Li6": "6Li", "Li7": "7Li", "Be7": "7Be", "Li8": "8Li",
        "Be8": "8Be", "B8": "8B",
    }
    by_name = {v: k for k, v in _NUBASE_NAMES.items()}
    excess, spin = {}, {}
    with open(nubase_path, encoding="latin-1") as fh:
        for line in fh:
            if line.startswith("#") or len(line) < 102:
                continue
            if line[7] != "0":
                continue
            key = by_name.get(line[11:16].strip())
            if key is None:
                continue
            excess[key] = float(line[18:31])
            spin[key] = _parse_spin(line[88:102])
    return excess, spin

def reaction_species(name):
    """Expand a reaction name into explicit reactant and product nuclide lists."""
    from pypr.nuclear import reaction_stoichiometry
    react, prod = reaction_stoichiometry(name)

    def expand(multiplicity_dict):
        out = []
        for species, count in multiplicity_dict.items():
            out += [species] * count
        return out

    return expand(react), expand(prod)

def detailed_balance(reactants, products, cfg):
    """Compute the reverse-rate coefficients (alpha, beta, gamma)."""
    keV, kB, MeV = cfg.keV, cfg.kB, cfg.MeV
    ma_e, me_e = cfg.ma * MeV, cfg.me * MeV
    NZ, EX, SP = cfg.Nuclides, cfg.NuclExcessMass, cfg.NuclSpin

    def mass(s):
        n, z = NZ[s]
        return (n + z) * ma_e + EX[s] * keV - z * me_e

    def binding(s):
        n, z = NZ[s]
        return n * EX["n"] + z * EX["p"] - EX[s]

    n_in, n_out = len(reactants), len(products)
    beta = 1.5 * (n_in - n_out)
    Q = keV * (sum(binding(s) for s in products) - sum(binding(s) for s in reactants))
    gamma = -Q / (kB * 1e9)

    def quantum_factor(side):
        val = 1.0
        for s, m in Counter(side).items():
            term = (2 * SP[s] + 1) * (2 * np.pi / mass(s) / (kB * 1e9)) ** (-1.5)
            val *= term ** m / factorial(m)
        return val

    units = ((ma_e / cfg.clight**2) / (cfg.hbar * cfg.clight)**3) ** (n_in - n_out)
    alpha = quantum_factor(reactants) / quantum_factor(products) * units
    return alpha, beta, gamma
